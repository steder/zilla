class ZillaSettingsError(exceptions.Exception):
    """Configuration file missing or unable to load all required settings"""


def load(path=None):
    if not path:
        path = os.path.join(os.curdir, "zilla.conf")

    if not os.path.exists(path):
        raise ZillaSettingsError("Missing config file: %s"%(path,))

    config_file = open(path, "r")
    config = yaml.safe_load(config_file.read())

    settings = {}
    for key,value in config.iteritems():
        setting = key.upper()
        settings[setting] = value

    g = globals()
    # let's only load config that we've got variables defined in this namespace
    for key in g:
        if key in settings:
            g[key] = settings[key]
    return settings


load()


